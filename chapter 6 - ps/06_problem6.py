a = int(input("Enter your marks: "))

if(a>=90 and a<=100):
    print("Your grade is Ex: ",a)

elif(a>=80 and a<90):
    print("Your grade is A: ",a)

elif(a>=70 and a<80):
    print("Your grade is B: ",a)

elif(a>=60 and a<70):
    print("Your grade is C: ",a)

elif(a>=50 and a<60):
    print("Your grade is D: ",a)

elif(a<=50):
    print("Your grade is F: ",a)

